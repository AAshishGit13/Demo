#include <stdio.h>

int main() {
    int a = 10;
    int b = 5;
    
    // Addition
    int sum = a + b;
    printf("Addition: %d + %d = %d\n", a, b, sum);
    
    // Subtraction
    int difference = a - b;
    printf("Subtraction: %d - %d = %d\n", a, b, difference);
    
    // Multiplication
    int product = a * b;
    printf("Multiplication: %d * %d = %d\n", a, b, product);
    
    return 0;
}
